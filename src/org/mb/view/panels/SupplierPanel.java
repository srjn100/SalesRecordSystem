    private void jTable_SupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_SupplierMouseClicked
        int row = jTable_Supplier.getSelectedRow();
        String[] options = {"Edit", "Delete", "Cancel"};
        int x = JOptionPane.showOptionDialog(null, "Choose an option.", "Supplier Option",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, null);
        
        if (x == 0) {
            Supplier sob = new Supplier();
            sob.setSupplier_id(Integer.parseInt(jTable_Supplier.getValueAt(row, 0).toString()));
            sob.setName(jTable_Supplier.getValueAt(row, 1).toString());
            sob.setAddress(jTable_Supplier.getValueAt(row, 2).toString());
            sob.setPhone(Long.parseLong(jTable_Supplier.getValueAt(row, 3).toString()));
            sob.setEmail(jTable_Supplier.getValueAt(row, 4).toString());
            
            SupplierForm s = new SupplierForm(hf, true);
            s.setSupplierPanel(this);
            s.setSupplier(sob);
            s.setVisible(true);
        }
    }//GEN-LAST:event_jTable_SupplierMouseClicked


