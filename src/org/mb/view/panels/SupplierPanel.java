    public void loadData() {
        clearModel();
        SupplierDAO sDAO = new SupplierDAO();
        ArrayList<Supplier> sList = sDAO.fetchData();
        for (int i = 0; i < sList.size(); i++) {
            model.addRow(new Object[]{sList.get(i).getSupplier_id(),
                sList.get(i).getName(), sList.get(i).getAddress(),
                sList.get(i).getPhone(), sList.get(i).getEmail()});
        }
    }
    
    public void clearModel() {
        int c = model.getRowCount();
        for (int i = 0; i < c; i++) {
            model.removeRow(0);
        }
    }

