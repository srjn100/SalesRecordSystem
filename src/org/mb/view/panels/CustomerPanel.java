    public void loadData() {
        clearModel();
        CustomerDAO cDAO = new CustomerDAO();
        ArrayList<Customer> cList = cDAO.fetchData();
        for (int i = 0; i < cList.size(); i++) {
            model.addRow(new Object[]{cList.get(i).getCustomer_id(),
                cList.get(i).getName(), cList.get(i).getAddress(),
                cList.get(i).getPhone(), cList.get(i).getEmail()});
        }
    }

    public void clearModel() {
        int c = model.getRowCount();
        for (int i = 0; i < c; i++) {
            model.removeRow(0);
        }
    }

