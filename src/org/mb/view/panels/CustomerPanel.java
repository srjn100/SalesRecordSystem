    private void jTable_CustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_CustomerMouseClicked
        int row = jTable_Customer.getSelectedRow();
        String[] options = {"Edit", "Delete", "Cancel"};
        int x = JOptionPane.showOptionDialog(null, "Choose an option.", "Customer Option",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, null);

        if (x == 0) {
            Customer cob = new Customer();
            cob.setCustomer_id(Integer.parseInt(jTable_Customer.getValueAt(row, 0).toString()));
            cob.setName(jTable_Customer.getValueAt(row, 1).toString());
            cob.setAddress(jTable_Customer.getValueAt(row, 2).toString());
            cob.setPhone(Long.parseLong(jTable_Customer.getValueAt(row, 3).toString()));
            cob.setEmail(jTable_Customer.getValueAt(row, 4).toString());
            
            CustomerForm c = new CustomerForm(hf, true);
            c.setCustomerPanel(this);
            c.setCustomer(cob);
            c.setVisible(true);
        }
    }//GEN-LAST:event_jTable_CustomerMouseClicked


