    private void jTable_PurchaseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_PurchaseMouseClicked
        int row = jTable_Purchase.getSelectedRow();
        String[] options = {"Edit", "Delete", "Cancel"};
        int x = JOptionPane.showOptionDialog(null, "Choose an option.", "Purchase Option",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, null);
        if (x == 0) {
            PurchasedItem pob = new PurchasedItem();
            pob.setProduct_id(Integer.parseInt(jTable_Purchase.getValueAt(row, 0).toString()));
            pob.setProduct(jTable_Purchase.getValueAt(row, 1).toString());
            pob.setQuantity(Integer.parseInt(jTable_Purchase.getValueAt(row, 2).toString()));
            pob.setMfg_date(java.sql.Date.valueOf((jTable_Purchase.getValueAt(row, 3).toString())));
            pob.setExp_date(java.sql.Date.valueOf((jTable_Purchase.getValueAt(row, 4).toString())));
            pob.setPurchase_price(Double.parseDouble(jTable_Purchase.getValueAt(row, 5).toString()));

            PurchaseItemForm p = new PurchaseItemForm(hf, true);
            p.setPurchaseForm(this);
            p.setPurchasedItem(pob);
            p.setVisible(true);
        } else if (x == 1) {
            purchase_model.removeRow(row);
            totalPrice();
        }
    }//GEN-LAST:event_jTable_PurchaseMouseClicked

    private void jTextField_PaymentKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_PaymentKeyReleased
        if (jTextField_Payment.getText().equals("")) {
            jLabel_PaymentError.setText(" ");
            jLabel_Due.setText(jLabel_Total.getText());
            return;
        }

        try {
            double pmt = Double.parseDouble(jTextField_Payment.getText());
            double total = Double.parseDouble(jLabel_Total.getText());
            if (pmt > total) {
                jLabel_PaymentError.setText("Invalid Payment");
            } else {
                jLabel_Due.setText((nf.format(total - pmt) + "").replaceAll(",", ""));
                jLabel_PaymentError.setText(" ");
            }
        } catch (Exception ex) {
            jLabel_PaymentError.setText("Invalid Payment");
        }
    }//GEN-LAST:event_jTextField_PaymentKeyReleased

    private void jButton_SavePurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SavePurchaseActionPerformed
        if (!jLabel_PaymentError.getText().equals(" ")) {
            JOptionPane.showMessageDialog(null, "Sorry there are errors in the form");
            return;
        }
        if (jComboBox_Supplier.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, " Please select a supplier first ");
            return;
        }
        if(jTable_Purchase.getRowCount()==0){
            JOptionPane.showMessageDialog(null, " Please Add products to purchase first ");
            return;
        }

        //add new purchase
        Purchase pob = new Purchase();
        pob.setPayment_status(Double.parseDouble(jLabel_Due.getText()) == 0);
        pob.setSupplier_id(smap.get(jComboBox_Supplier.getSelectedItem().toString()));
        pob.setUpdated_by(uID);

        PurchaseDAO pDAO = new PurchaseDAO();
        int purchase_id = pDAO.addPurchase(pob);

        if (purchase_id != -1) {
            //add purchase items and stock
            ArrayList<PurchasedItem> pList = new ArrayList<>();
            int c = purchase_model.getRowCount();
            for (int row = 0; row < c; row++) {
                PurchasedItem ob = new PurchasedItem();
                ob.setProduct_id(Integer.parseInt(jTable_Purchase.getValueAt(row, 0).toString()));
                ob.setPurchase_id(purchase_id);
                ob.setQuantity(Integer.parseInt(jTable_Purchase.getValueAt(row, 2).toString()));
                ob.setPurchase_price(Double.parseDouble(jTable_Purchase.getValueAt(row, 5).toString()));
                ob.setMfg_date(java.sql.Date.valueOf((jTable_Purchase.getValueAt(row, 3).toString())));
                ob.setExp_date(java.sql.Date.valueOf((jTable_Purchase.getValueAt(row, 4).toString())));

                pList.add(ob);
            }
            PurchasedItemDAO piDAO = new PurchasedItemDAO();
            piDAO.addPurchasedItem(pList);

            //add payment
            Payment pmob = new Payment();
            PaymentDAO pmDAO = new PaymentDAO();
            pmob.setPurchase_id(purchase_id);
            pmob.setAmount(Double.parseDouble(jTextField_Payment.getText()));
            pmob.setUpdated_by(uID);
            pmDAO.addPayment(pmob);

            JOptionPane.showMessageDialog(null, " Purchase record success full");
            pp.loadData();
            dispose();
        }
    }//GEN-LAST:event_jButton_SavePurchaseActionPerformed

