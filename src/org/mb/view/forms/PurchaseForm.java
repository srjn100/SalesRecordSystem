    public void loadSupplier() {
        SupplierDAO sDAO = new SupplierDAO();
        smap = sDAO.fetchSupplier();
        for (Map.Entry<String, Integer> m : smap.entrySet()) {
            jComboBox_Supplier.addItem(m.getKey());
        }
    }

    public int checkProduct(int productID) {
        for (int i = 0; i < jTable_Purchase.getRowCount(); i++) {
            if (jTable_Purchase.getValueAt(i, 0).toString().equalsIgnoreCase(productID + "")) {
                return i;
            }
        }
        return -1;
    }

    public void totalPrice() {
        int c = purchase_model.getRowCount();
        double total = 0;
        for (int i = 0; i < c; i++) {
            total += Double.parseDouble(purchase_model.getValueAt(i, 6).toString().replaceAll(",", ""));
        }
        jLabel_Total.setText(total + "");
        jTextField_Payment.setText("0.00");
        jLabel_PaymentError.setText(" ");
        jLabel_Due.setText((total + "").replaceAll(",", ""));
    }

