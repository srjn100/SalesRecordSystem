    public void loadProductData() {
        clearProductModel();
        ProductDAO pDAO = new ProductDAO();
        ArrayList<Product> pList = pDAO.fetchData();
        for (int i = 0; i < pList.size(); i++) {

            productModel.addRow(new Object[]{pList.get(i).getProduct_id(),
                pList.get(i).getName(), pList.get(i).getDescription(),
                pList.get(i).getSales_price(), pList.get(i).getCategory(),
                pList.get(i).getQuantity()});
        }
    }

    public void clearProductModel() {
        int c = productModel.getRowCount();
        for (int i = 0; i < c; i++) {
            productModel.removeRow(0);
        }
    }

