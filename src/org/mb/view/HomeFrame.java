    private void jTable_ProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_ProductMouseClicked
        int row = jTable_Product.getSelectedRow();
        String[] options = {"Edit", "Delete", "Cancel"};
        int x = JOptionPane.showOptionDialog(null, "Choose an option.", "Product Option",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, null);
        if (x == 0) {
            Product pob = new Product();
            pob.setProduct_id(Integer.parseInt(jTable_Product.getValueAt(row, 0).toString()));
            pob.setName(jTable_Product.getValueAt(row, 1).toString());
            pob.setDescription(jTable_Product.getValueAt(row, 2).toString());
            pob.setSales_price(Double.parseDouble(jTable_Product.getValueAt(row, 3).toString()));
            pob.setCategory(jTable_Product.getValueAt(row, 4).toString());

            ProductForm p = new ProductForm(this, true);
            p.setHomeFrame(this);
            p.setProduct(pob);
            p.setVisible(true);
        }

    }//GEN-LAST:event_jTable_ProductMouseClicked

